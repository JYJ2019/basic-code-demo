package Day01;

public class ConstantDemo {
    public static void main(String[] args) {
        //输出整数常量
        System.out.println(1);
        //输出小数常量
        System.out.println(0.0);
        //输出字符常量
        System.out.println('2');
        //输出布尔常量
        System.out.println(true);//或者false
        //输出字符串常量
        System.out.println("你好!");

        //空常量null无法输出
    }
}
